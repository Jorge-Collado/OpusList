    private void btnEscogerImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscogerImagenActionPerformed
        fileChooser = new JFileChooser();
        int returnOption = fileChooser.showOpenDialog(this);
        if (returnOption == JFileChooser.APPROVE_OPTION){
            imagePath = fileChooser.getSelectedFile().getAbsolutePath();
            imageName = fileChooser.getSelectedFile().getName();
        }
    }//GEN-LAST:event_btnEscogerImagenActionPerformed

    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        for (Obra o : mf.obras){
               if(o.getRegistre().equals(txtRegistro.getText())){
                   try{
                   o.setTitol(txtTitulo.getText());
                   o.setAny(txtYear.getText());
                   o.setFormat(txtFormato.getText());
                   o.setAutor(txtAutor.getText());  
                   BufferedImage bufferedImage = ImageIO.read(new File(imagePath));
                   String output = System.getProperty("user.home") + "\\AppData\\Local\\OpusList\\images\\" + imageName;
                   File outputImage = new File(output);
                   ImageIO.write(bufferedImage, "jpg", outputImage);
                   o.setImatge(imageName);
                   }catch(IOException ioe){
                       ioe.printStackTrace();
                   }
               }
        }
        dispose();
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

    private void MostrarComponentes(boolean modoEdicion){
        if (modoEdicion) {
            txtTitulo.setVisible(true);
            txtYear.setVisible(true);
            txtFormato.setVisible(true);
            txtAutor.setVisible(true);
            lblAutor.setVisible(true);
            lblYear.setVisible(true);
            lblTitulo.setVisible(true);
            lblFormato.setVisible(true);
            txtRegistro.setEditable(false);
        }
    }
    
